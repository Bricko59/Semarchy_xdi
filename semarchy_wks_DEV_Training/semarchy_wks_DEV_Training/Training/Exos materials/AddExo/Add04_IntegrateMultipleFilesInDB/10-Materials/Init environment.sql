DROP SCHEMA LOAD_FILES IF EXISTS CASCADE;
CREATE SCHEMA INTEGRATED_FILES;

CREATE TABLE INTEGRATED_FILES.DATA_FILE
(
id_record   INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
file_id     INTEGER,
last_name   VARCHAR(100),
first_name  VARCHAR(100),
file_name   VARCHAR(100),
file_date   CHAR(8),
update_date TIMESTAMP);

